-- ----- KRT Lua Contract ----- --
-- deps: local addon = select(2, ...)
-- shared: static raid item source data for Vanilla through WotLK
-- exports: addon.LootSourcesData

local addon = select(2, ...)

-- ----- Internal state ----- --
addon.LootSourcesData = addon.LootSourcesData or {}
addon.LootSourcesData.ByItemId = addon.LootSourcesData.ByItemId or {}

local ByItemId = addon.LootSourcesData.ByItemId

-- ----- Private helpers ----- --
local function addItemSource(itemId, source, raidName)
    local itemSources = ByItemId[itemId]
    if not itemSources then
        itemSources = {}
        ByItemId[itemId] = itemSources
    end

    itemSources[#itemSources + 1] = {
        npcId = source.npcId,
        npcName = source.name,
        raid = raidName,
        kind = source.kind or "boss",
        modes = source.modes,
    }
end

local function markSharedItemSources()
    for _, itemSources in pairs(ByItemId) do
        local bossKeys = {}
        local bossCount = 0
        for i = 1, #itemSources do
            local source = itemSources[i]
            if source and source.kind == "boss" then
                local key = tostring(source.raid or "") .. ":" .. tostring(source.npcId or "")
                if not bossKeys[key] then
                    bossKeys[key] = true
                    bossCount = bossCount + 1
                end
            end
        end

        if bossCount > 1 then
            for i = 1, #itemSources do
                local source = itemSources[i]
                if source and source.kind == "boss" then
                    source.shared = true
                    source.note = "Shared"
                end
            end
        end
    end
end

local function loadRaidLootSources(raidLootSources)
    for i = 1, #raidLootSources do
        local raid = raidLootSources[i]
        local raidName = raid.name
        local sources = raid.sources or {}
        for j = 1, #sources do
            local source = sources[j]
            local items = source.items or {}
            for k = 1, #items do
                local item = items[k]
                addItemSource(item[1], {
                    npcId = source.npcId,
                    name = source.name,
                    kind = source.kind,
                    modes = item[2],
                }, raidName)
            end
        end
    end
end

